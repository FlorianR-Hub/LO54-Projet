CREATE TABLE VIDEO.ACTOR (
	ID_ACTOR INTEGER GENERATED ALWAYS AS IDENTITY NOT NULL,
	LASTNAME VARCHAR(20) NOT NULL,
	FIRSTNAME VARCHAR(20) DEFAULT 'NULL',
	PRIMARY KEY (ID_ACTOR)
);

CREATE TABLE VIDEO.MOVIE (
	ID_MOVIE INTEGER GENERATED ALWAYS AS IDENTITY NOT NULL,
	TITLE VARCHAR(50) NOT NULL,
	COPIES INTEGER NOT NULL,
	MOVIE_TYPE VARCHAR(20) NOT NULL,
	MAIN_ACTOR_ID INTEGER DEFAULT NULL,
	PRIMARY KEY (ID_MOVIE)
);

CREATE TABLE VIDEO.MOVIE_SEC_ACTORS (
	ID_MOVIE INTEGER NOT NULL,
	ID_ACTOR INTEGER NOT NULL,
	PRIMARY KEY (ID_ACTOR,ID_MOVIE)
);

ALTER TABLE VIDEO.MOVIE
	ADD FOREIGN KEY (MAIN_ACTOR_ID) 
	REFERENCES VIDEO.ACTOR (ID_ACTOR);

ALTER TABLE VIDEO.MOVIE_SEC_ACTORS
	ADD FOREIGN KEY (ID_ACTOR) 
	REFERENCES VIDEO.ACTOR (ID_ACTOR);

ALTER TABLE VIDEO.MOVIE_SEC_ACTORS
	ADD FOREIGN KEY (ID_MOVIE) 
	REFERENCES VIDEO.MOVIE (ID_MOVIE);

